expected<float, Error> getMagneticDeclination(float lat, float lon, uint16_t year)
{
	int8_t lati, loni;
	uint8_t year_end;
	
	lati = (int8_t)lat;
	loni = (int8_t)lon;
	year_end = year - 2000;
	
	for (size_t i = 0; i < numMagDecCorrections; i++)
	{ 
		if ((magDecCorrections[i].lat == lati) &&
		    (magDecCorrections[i].lon == loni) && 
		    (magDecCorrections[i].year == year_end))
		{
			return magDecCorrections[i].magdec;
		}
	}

	return expected<float, Error>{unexpected<Error>{Error::NotFound}};
	
}



